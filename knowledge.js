// RAG 检索函数：根据用户数据匹配相关知识
function retrieveKnowledge(userData) {
  const relevant = KNOWLEDGE_BASE.filter(chunk => {
    try { return chunk.match(userData); } catch { return false; }
  });
  return relevant.map(chunk => `## ${chunk.title}\n${chunk.content}`).join('\n\n');
}
